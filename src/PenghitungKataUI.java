    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan Teks dan Hasil");

        int pilihan = fileChooser.showSaveDialog(this);

        if (pilihan == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            if (!file.getAbsolutePath().endsWith(".txt")) {
                file = new File(file.getAbsolutePath() + ".txt");
            }

            try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {

                writer.write("--- TEKS PENGGUNA ---\n");
                writer.write(jTextAreaInput.getText());
                writer.newLine();
                writer.newLine();

                writer.write("--- HASIL PERHITUNGAN ---\n");

                writer.write("Jumlah Karakter: " + lblKarakter.getText() + "\n");
                writer.write("Jumlah Kata: " + lblKata.getText() + "\n");
                writer.write("Jumlah Kalimat: " + lblKalimat.getText() + "\n");
                writer.write("Jumlah Paragraf: " + lblParagraf.getText() + "\n");

                JOptionPane.showMessageDialog(this, "Hasil berhasil disimpan ke:\n" + file.getAbsolutePath());

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error: Gagal menyimpan file.\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        String kataCari = jTextFieldCari.getText();

        String teks = jTextAreaInput.getText();
        int jumlahDitemukan = 0;
        if (!kataCari.isEmpty() && !teks.isEmpty()) {
            String[] kata = teks.split("[\\s.,!?]+");

            for (String k : kata) {
                if (k.equalsIgnoreCase(kataCari)) {
                    jumlahDitemukan++;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Kata '" + kataCari + "' ditemukan sebanyak " + jumlahDitemukan + " kali.");
    }//GEN-LAST:event_jButtonCariActionPerformed

