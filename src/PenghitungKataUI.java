    private void hitungKataSaja() {
        String teks = jTextAreaInput.getText(); // Ganti jika nama JTextArea Anda beda
        
        int jumlahKata = 0;
        if (!teks.trim().isEmpty()) {
            // Ini adalah Ekspresi Reguler (Regex)
            String[] kata = teks.trim().split("\\s+"); // Pecah berdasarkan spasi
            jumlahKata = kata.length;
        }
        
        // Update HANYA lblKata
        lblKata.setText(String.valueOf(jumlahKata));
    }
    
    private void hitungSemua() {
        // 1. Ambil teks dari JTextArea
        String teks = jTextAreaInput.getText(); // Ganti jika nama JTextArea Anda beda

        // 2. Hitung Karakter
        int jumlahKarakter = teks.length();

        // 3. (Bagian Hitung Kata DIHAPUS dari sini)

        // 4. Hitung Kalimat (Variasi)
        int jumlahKalimat = 0;
        if (!teks.trim().isEmpty()) {
            String[] kalimat = teks.trim().split("[.!?]+"); 
            jumlahKalimat = kalimat.length;
        }

        // 5. Hitung Paragraf (Variasi)
        int jumlahParagraf = 0;
        if (!teks.trim().isEmpty()) {
            String[] paragraf = teks.trim().split("(\\n[\\s]*\\n)+|(\\r\\n[\\s]*\\r\\n)+");
            jumlahParagraf = paragraf.length;
        }

        // 6. Update tampilan JLabel (TANPA lblKata)
        lblKarakter.setText(String.valueOf(jumlahKarakter));
        lblKalimat.setText(String.valueOf(jumlahKalimat));
        lblParagraf.setText(String.valueOf(jumlahParagraf));
    }

